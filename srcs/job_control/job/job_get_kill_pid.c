#include <sys/wait.h>
#include <errno.h>
#include "job_control/job.h"

pid_t				job_get_kill_pid(t_job *job)
{
	pid_t			gid;

	if ((gid = getpgid(job->pid)) < 0)
		LOG_ERROR("getpgid: %s", strerror(errno));
	return (-gid);
}
