#include "tokenizer.h"

/*  Rule 11
	The current character is used as the start of a new word.
*/

int					tokenizer_apply_rule011(t_tokenizer *t)
{
	tokenizer_append_to_curtoken(t);
	t->tokentype = PREV_WORD;
	t->curpos++;
	return (1);
}
