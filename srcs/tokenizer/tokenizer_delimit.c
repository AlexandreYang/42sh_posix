#include <stdlib.h>

#include "tokenizer.h"

void				tokenizer_delimit(t_tokenizer *this)
{
	twl_lst_push(this->tokens, token_new(TOKEN_WORD, twl_strdup(this->curtoken)));
	this->curtoken[0] = '\0';
}
