#include "shenv/shvar.h"

char				*shvar_get_visible_export_value(t_shvar *this)
{
	if (this->shvar_assign_value)
		return (this->shvar_assign_value);
	if (this->shvar_exported)
		return (this->shvar_value);
	return (NULL);
}
