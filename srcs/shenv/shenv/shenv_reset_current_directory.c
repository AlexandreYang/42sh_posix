#include "shenv/shenv.h"

void			shenv_reset_current_directory(t_shenv *this, char *new_dir)
{
	if (this->shenv_current_directory != NULL)
	{
		free(this->shenv_current_directory);
	}
	this->shenv_current_directory = twl_strdup(new_dir);
}
