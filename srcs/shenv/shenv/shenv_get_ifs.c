#include "shenv/shenv.h"

char				*shenv_get_ifs(t_shenv *this)
{
	char			*ifs;

	ifs = shenv_shvars_get_value(this, "IFS");
	if (!ifs)
		ifs = SHENV_DEFAULT_IFS;
	return (ifs);
}
