#include "shenv/shenv.h"

char			*shenv_get_current_directory(t_shenv *this, char *from_whom)
{
	if (!this->shenv_current_directory)
	{
		shenv_set_current_directory(this, from_whom);
	}
	return (this->shenv_current_directory);
}
