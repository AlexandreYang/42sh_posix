#include "twl_xstdlib.h"

#include "terminal.h"

t_termios			*terminal_singleton(t_termios *term)
{
	static t_terminal *term_sav = NULL;

	if (!term_sav)
		term_sav = terminal_new(term);
	return (term_sav->term);
}
