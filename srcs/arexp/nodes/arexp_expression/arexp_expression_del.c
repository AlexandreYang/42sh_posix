#include "arexp/nodes/arexp_expression.h"

void			arexp_expression_del(t_arexp_expression *expression)
{
	if (!expression)
		return ;
	if (expression->assignment)
		twl_lst_del(expression->assignment, arexp_assignment_del);
	free(expression);
}
