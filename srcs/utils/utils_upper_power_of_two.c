#include "utils.h"

size_t				utils_upper_power_of_two(size_t num)
{
	if (utils_is_power_of_two(num))
		return (num << 1);
	num -= 1;
	num |= num >> 1;
	num |= num >> 2;
	num |= num >> 4;
	num |= num >> 8;
	num |= num >> 16;
	num |= num >> 32;
	num += 1;
	return (num);
}
