#include "utils.h"
#include <errno.h>

void				utils_log_errno(char *name, int error_code)
{
	int				errno_save;

	errno_save = errno;
	if (error_code < 0)
		LOG_ERROR("%s: %s", name, strerror(errno_save));
}
