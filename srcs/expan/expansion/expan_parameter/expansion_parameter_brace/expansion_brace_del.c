#include "expan/expansion_parameter_brace.h"

void				expansion_brace_del(t_expansion_brace *this)
{
	if (!this)
		return ;
	if (this->param)
		free(this->param);
	if (this->word)
		free(this->word);
	if (this->error)
		free(this->error);
	free(this);
}
