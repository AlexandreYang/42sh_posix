#include "expan/expan_utils.h"

bool			expan_is_special_parameter(char c)
{
	return (c == '@' ||
			c == '*' ||
			c == '#' ||
			c == '?' ||
			c == '-' ||
			c == '$' ||
			c == '!' ||
			c == '0');
}
