#include "pattern.h"

char				pattern_get_begin_file(t_pattern *this)
{
	const t_pattern_data	*first = twl_lst_first(this->split);

	if (!first)
		return (0);
	if (first->split[0] == '/')
		return ('/');
	return ('.');
}
