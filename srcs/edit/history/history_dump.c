#include "edit/history.h"

void			history_dump(t_history *this)
{
	t_histlist	*tmp;

	tmp = this->first;
	while (tmp)
	{
		twl_printf("%5zu  %s\n", tmp->number, tmp->line->copy);
		tmp = tmp->next;
	}
}
