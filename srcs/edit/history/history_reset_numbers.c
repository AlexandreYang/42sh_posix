#include "edit/history.h"

void			history_reset_numbers(t_history *this)
{
	t_histlist	*tmp;
	size_t		index;

	tmp = this->first;
	index = 1;
	while (tmp)
	{
		tmp->number = index;
		index += 1;
		tmp = tmp->next;
	}
}
