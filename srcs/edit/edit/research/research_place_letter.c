#include "edit/research.h"

void			research_place_letter(t_edit *this, unsigned char buf)
{
	this->research->search[this->research->size] = buf;
	this->research->size += 1;
	if (this->research->size == this->research->total)
	{
		research_realloc(this->research);
	}
}
