#include "edit/edit.h"

void			edit_print_current_char_and_move(t_edit *this)
{
	if (this->pos_cursor == this->current->size)
		return ;
	this->putc(this->current->line[this->pos_cursor]);
	this->pos_cursor += 1;
}
